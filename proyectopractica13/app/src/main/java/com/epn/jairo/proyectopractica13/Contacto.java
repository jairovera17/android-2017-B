public class Contacto {


    public String id;
    public String name;
    public String email;
    public String address;
    public String gender;

    public Contacto(String id, String name, String email, String address, String gender) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.address = address;
        this.gender = gender;
    }
}
